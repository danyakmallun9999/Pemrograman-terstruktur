# Menampilkan Mahasiswa yang sudah masuk ruang ujian

nilai_pengulangan = int(input("Masukkan Nilai Pengulangan : "))

for i in range (nilai_pengulangan):
    print("Mahasiswa ke",i+1,"sudah memasuki ruangan ujian akhir semester")