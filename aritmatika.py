# Belajar operasi aritmatika di pyhton
# Operasi aritmatika

# variable nilai
nilai_1 = 29
nilai_2 = 7

# Operasi penjumlahan pada python
hasil_nilai = nilai_1 + nilai_2
print(nilai_1, '+' , nilai_2, '=', hasil_nilai)

# Operasi pengurangan pada python
hasil_nilai = nilai_1 - nilai_2
print(nilai_1, '-' , nilai_2, '=', hasil_nilai)

# Operasi perkalian pada python
hasil_nilai = nilai_1 * nilai_2
print(nilai_1, '*' , nilai_2, '=', hasil_nilai)

# Operasi pembagian pada python
hasil_nilai = nilai_1 / nilai_2
print(nilai_1, '/' , nilai_2, '=', hasil_nilai)

# Operasi sisa bagi pada python
hasil_nilai = nilai_1 % nilai_2
print(nilai_1, '%' , nilai_2, '=', hasil_nilai)

# Operasi perpangkatan pada python
hasil_nilai = nilai_1 ** nilai_2
print(nilai_1, '**' , nilai_2, '=', hasil_nilai)

# Operasi floor division pada python
hasil_nilai = nilai_1 // nilai_2
print(nilai_1, '/' , nilai_2, '=', hasil_nilai)